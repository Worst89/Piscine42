int	ft_is_prime(int nb)
{
	int		i;

	if (nb == 2)
		return (1);
	if (nb <= 1 || !(nb % 2))
		return (0);
	i = 2;
	while (i <= nb / 2)
	{
		if (!(nb % i))
			return (0);
		i += 1;
	}
	return (1);
}
