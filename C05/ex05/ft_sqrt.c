int	ft_sqrt(int nb)
{
	int	i;
	int	pow;

	if (nb < 1)
		return (0);
	else if (nb == 1)
		return (1);
	i = 2;
	pow = 0;
	while (i < nb && i < 46341)
	{
		pow = i * i;
		if (pow == nb)
			return (i);
		i++;
	}
	return (0);
}
