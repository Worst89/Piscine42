int	ft_atoi(char *str)
{
	int	x;
	int	z;

	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	z = 1;
	while (*str == 45 || *str == 43)
		if (*str == 45)
			z = -z;
	x = 0;
	while (*str >= '0' && *str <= '9')
		x = x * 10 + *str++ - 48;
	return (x * z);
}
