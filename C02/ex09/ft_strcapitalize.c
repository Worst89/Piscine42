char	*ft_strcapitalize(char *str)
{
	int	x;

	x = 0;
	if (str[x] >= 97 && str[x] <= 122)
	{
		str[x] = str[x] - 32;
	}
	x = 1;
	while (str[x] != '\0')
	{
		if (((str[x - 1] >= 32) && (str[x - 1] <= 47))
			|| ((str[x - 1] >= 58) && (str[x - 1] <= 64))
			|| ((str[x - 1] >= 91) && (str[x - 1] <= 96))
			|| ((str[x - 1] >= 123) && (str[x - 1] <= 127)))
		{
			if (str[x] >= 97 && str[x] <= 122)
				str[x] = str[x] - 32;
			else if (str[x] >= 65 && str[x] <= 90)
			{
				str[x] = str[x] + 32;
			}
		}
		x++;
	}
	return (str);
}
